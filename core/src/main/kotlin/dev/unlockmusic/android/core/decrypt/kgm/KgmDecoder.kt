package dev.unlockmusic.android.core.decrypt.kgm

class KgmDecoder(
    private val file: ByteArray,
    private val extension: String,
) {
    fun decrypt(): ByteArray {
        val mode = KgmMode.fromExtension(extension)
        require(file.size >= MIN_HEADER_SIZE) { "KGM file is too short" }
        require(file.copyOfRange(0, HEADER_SIZE).contentEquals(mode.header)) {
            "Invalid ${mode.label} header"
        }

        val headerLength = readUInt32LittleEndian(file, HEADER_LENGTH_OFFSET)
        require(headerLength in MIN_HEADER_SIZE..file.size) { "Invalid KGM header length" }

        val key =
            ByteArray(KEY_SIZE + 1).apply {
                System.arraycopy(file, KEY_OFFSET, this, 0, KEY_SIZE)
            }
        val encryptedAudio = file.copyOfRange(headerLength, file.size)

        encryptedAudio.indices.forEach { index ->
            encryptedAudio[index] = decryptByte(encryptedAudio[index], key, index, mode)
        }

        return encryptedAudio
    }

    private fun decryptByte(
        value: Byte,
        key: ByteArray,
        offset: Int,
        mode: KgmMode,
    ): Byte {
        var medium = key[offset % key.size].toInt().and(0xFF) xor value.toInt().and(0xFF)
        medium = medium xor ((medium and 0x0F) shl 4)

        var mask = getMask(offset)
        mask = mask xor ((mask and 0x0F) shl 4)

        var result = medium xor mask
        if (mode == KgmMode.Vpr) {
            result = result xor VPR_MASK_DIFF[offset % VPR_MASK_DIFF.size]
        }
        return result.toByte()
    }

    private fun getMask(position: Int): Int {
        var offset = position ushr 4
        var value = 0
        while (offset >= 0x11) {
            value = value xor TABLE_1[offset % MASK_TABLE_SIZE]
            offset = offset ushr 4
            value = value xor TABLE_2[offset % MASK_TABLE_SIZE]
            offset = offset ushr 4
        }

        return MASK_V2_PRE_DEF[position % MASK_TABLE_SIZE] xor value
    }

    private fun readUInt32LittleEndian(
        source: ByteArray,
        offset: Int,
    ): Int {
        require(offset + 4 <= source.size) { "Unexpected end of KGM file" }
        return (source[offset].toInt().and(0xFF)) or
            (source[offset + 1].toInt().and(0xFF) shl 8) or
            (source[offset + 2].toInt().and(0xFF) shl 16) or
            (source[offset + 3].toInt().and(0xFF) shl 24)
    }

    private enum class KgmMode(
        val header: ByteArray,
        val label: String,
    ) {
        Kgm(KGM_HEADER, "kgm"),
        Vpr(VPR_HEADER, "vpr"),
        ;

        companion object {
            fun fromExtension(extension: String): KgmMode {
                return if (extension == "vpr") Vpr else Kgm
            }
        }
    }

